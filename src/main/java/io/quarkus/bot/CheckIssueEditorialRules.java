package io.quarkus.bot;

import java.io.IOException;

import javax.inject.Inject;

import org.jboss.logging.Logger;
import org.kohsuke.github.GHEventPayload;
import org.kohsuke.github.GHIssue;

import io.quarkiverse.githubapp.event.Issue;
import io.quarkus.bot.config.QuarkusBotConfig;

public class CheckIssueEditorialRules {
    private static final Logger LOG = Logger.getLogger(CheckIssueEditorialRules.class);

    private static final String ZULIP_URL = "https://quarkusio.zulipchat.com/";
    public static final String ZULIP_WARNING = "You added a link to a Zulip discussion, please make sure the description of the issue is comprehensive and doesn't require accessing Zulip."
            + "\n\n> This message is automatically generated by a bot.";

    @Inject
    QuarkusBotConfig quarkusBotConfig;

    void onOpen(@Issue.Opened GHEventPayload.Issue issuePayload) throws IOException {
        GHIssue issue = issuePayload.getIssue();
        String body = issue.getBody();

        if (!body.contains(ZULIP_URL)) {
            return;
        }

        if (!quarkusBotConfig.isDryRun()) {
            issue.comment(ZULIP_WARNING);
        } else {
            LOG.info("Issue #" + issue.getNumber() + " - Add comment " + ZULIP_WARNING);
        }
    }
}
